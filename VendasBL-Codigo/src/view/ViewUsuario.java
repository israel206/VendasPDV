    /**
     * habilitar e desabilitar campos e botoes do formulario
     * @param condicao 
     */
    private void habilitarDesabilitarCampos(boolean condicao){
        txt_usuNome.setEnabled(condicao);
        txt_usuLogin.setEnabled(condicao);
        jPassF_usuSenha.setEnabled(condicao);
        jB_salvar.setEnabled(condicao);
        jB_excluir.setEnabled(condicao);
        jB_cancelar.setEnabled(condicao);
    }
    /**
     * carregar usuários na tabela
     */
    private void carregarUsuario(){
        listaModelUsuarios = controllerUsuario.getListaUsuarioController();
        //Usando o modelo padrão de tabela
        DefaultTableModel modelo = (DefaultTableModel)tb_Usuario.getModel();
        //zerando linha 0 e setando os dados
        modelo.setNumRows(0);
        //inserir produtos na tabela
        int cont = listaModelUsuarios.size();
        //contador de tamanho da lista
        for (int i = 0; i < cont; i++) {
            //preenchendo col. e linha.
            modelo.addRow(new Object[]{
                listaModelUsuarios.get(i).getIdUsuario(),
                listaModelUsuarios.get(i).getUsuNome(),
                listaModelUsuarios.get(i).getUsuLogin(),
            });
        }
    }
    
