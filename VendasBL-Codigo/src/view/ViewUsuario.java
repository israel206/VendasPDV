    private void jB_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_salvarActionPerformed
        
        try {
            modelUsuario.setIdUsuario(Integer.parseInt(txt_usuCod.getText()));
        } catch (NumberFormatException e) {
            //JOptionPane.showMessageDialog(rootPane, "Erro ao no código!", "ERRO:", JOptionPane.ERROR_MESSAGE);
           //e.printStackTrace();
        }
        
        modelUsuario.setUsuNome(txt_usuNome.getText());
        modelUsuario.setUsuLogin(txt_usuLogin.getText());
        modelUsuario.setUsuSenha(jPassF_usuSenha.getText());
        
        if(salvarAlterar.equals("salvar")){
            
            if(controllerUsuario.salvarUsuarioController(modelUsuario) > 0){
                JOptionPane.showMessageDialog(rootPane, "Usuário cadastrado com sucesso!", "ATENÇÃO!", JOptionPane.WARNING_MESSAGE);
                
                this.carregarUsuario();
                this.habilitarDesabilitarCampos(false);
                this.limparCampos();
                
            }else{
                JOptionPane.showMessageDialog(rootPane, "Erro ao cadastrar Usuário!", "ERRO:", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            
            if(controllerUsuario.atualizarUsuarioController(modelUsuario)){
                JOptionPane.showMessageDialog(rootPane, "Usuário atualizado com sucesso!", "ATENÇÃO!", JOptionPane.WARNING_MESSAGE);
                
                this.carregarUsuario();
                this.habilitarDesabilitarCampos(false);
                this.limparCampos();
                
            }else{
                JOptionPane.showMessageDialog(rootPane, "Erro ao atualizar Usuário!", "ERRO:", JOptionPane.ERROR_MESSAGE);
            }
        }
    
    }//GEN-LAST:event_jB_salvarActionPerformed

    private void jB_alterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_alterarActionPerformed
        salvarAlterar = "alterar";
        int linha = this.tb_Usuario.getSelectedRow();
        try {
            int codigoUsuario = (int) this.tb_Usuario.getValueAt(linha, 0);
            //Recuperar dados do banco
            modelUsuario = controllerUsuario.getUsuarioController(codigoUsuario);
            //Setar na interface
            txt_usuCod.setText(String.valueOf(modelUsuario.getIdUsuario()));
            txt_usuNome.setText(modelUsuario.getUsuNome());
            txt_usuLogin.setText(modelUsuario.getUsuLogin());
            jPassF_usuSenha.setText(modelUsuario.getUsuSenha());
            
            this.habilitarDesabilitarCampos(true);
            this.limparCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Código inválido ou nenhum registro selecionado.", "AVISO!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jB_alterarActionPerformed

    private void jB_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_excluirActionPerformed
        //selecionar a linha
        int linha = tb_Usuario.getSelectedRow();
        //pegando o codigo da coluna da linha selecionada
        int codigoUsuario = (int) tb_Usuario.getValueAt(linha, 0);
        
        if(controllerUsuario.excluirUsuarioController(codigoUsuario)){
           JOptionPane.showMessageDialog(rootPane, "Usuário excluído com sucesso!", "ATENÇÃO!", JOptionPane.WARNING_MESSAGE);
            this.carregarUsuario();
            this.habilitarDesabilitarCampos(false);
            this.limparCampos();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Erro ao excluir Usuário!", "ERRO:", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jB_excluirActionPerformed

