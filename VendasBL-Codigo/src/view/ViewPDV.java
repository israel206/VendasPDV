    /**
     * Salvando vendas
     */
    private void salvarVenda(){
        int cont;
        int codigoProduto = 0, codigoVenda = 0;
        modelVendas = new ModelVendas();
        modelVendas.setCliente(1);
        try {
             modelVendas.setVenDataVenda(bLDatas.converterDataParaDateUS(new java.util.Date(System.currentTimeMillis())));
        } catch (Exception ex) {
        }
        //adicionando
        modelVendas.setVenValorBruto(Double.parseDouble(jFTF_ValorBruto.getText()));
        modelVendas.setVenDesconto(viewPagamentoPDV.getDesconto());
        modelVendas.setVenValorLiquido(blMascara.arredondamentoComPontoDuasCasas(viewPagamentoPDV.getValorTotal()));
        //Salvando no banco a venda
        codigoVenda = controllerVendas.salvarVendasController(modelVendas);

        //SALVANDO PRODUTOS
        cont = jTbPDV.getRowCount();
        for (int i = 0; i < cont; i++) {
            //salvando os produto no estoque vendas dos produtos
            codigoProduto = (int)jTbPDV.getValueAt(i, 1);
            modelVendasProdutos = new ModelVendasProdutos();
            modelProdutos = new ModelProdutos();
            //adicionando venda produto
            modelVendasProdutos.setProduto(codigoProduto);
            modelVendasProdutos.setVendas(codigoVenda);
            modelVendasProdutos.setVenProValor((double)jTbPDV.getValueAt(i, 4));
            modelVendasProdutos.setVenProQuantidade(Integer.parseInt(jTbPDV.getValueAt(i, 3).toString()));

            //produto, dando baixa no estoque de vendas dos produtos
            modelProdutos.setIdProduto(codigoProduto);
            //retornando estoque atual e dando baixa
            modelProdutos.setProEstoque(controllerProdutos.retornarProdutoController(codigoProduto).getProEstoque() 
            - Integer.parseInt(jTbPDV.getValueAt(i, 3).toString()));

            //Adicinando
            listaModelVendasProdutos.add(modelVendasProdutos); 
            listaModelProdutos.add(modelProdutos);
        }
        //Salvar os produtos da venda
        if(controllerVendasProdutos.salvarVendasProdutosController(listaModelVendasProdutos)){
            //alterar o estoque de produtos
            controllerProdutos.alterarEstoqueProdutoController(listaModelProdutos);
            //JOptionPane.showMessageDialog(rootPane, "produtos da venda salvo com sucesso!", "ATEN√á√ÉO!", JOptionPane.WARNING_MESSAGE);
            //Retornando para 1 depois da soma.
            this.limparTela();
            
        }else{
            JOptionPane.showMessageDialog(rootPane, "Erro ao salva produto da venda!", "ERRO:", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    /**
     * Pegando conteudos e add o produto
     * @param e 
     */
    private void pegarConteudo(java.awt.event.KeyEvent e){
        //Status da label no PDV do Caixa
        jLb_Status.setText("Venda em aberta!");
        
        DefaultTableModel modelo = (DefaultTableModel)jTbPDV.getModel();
        if(e.getKeyCode() == java.awt.event.KeyEvent.VK_ENTER){
            try {
                modelProdutos = controllerProdutos.retornarProdutoController(Integer.parseInt(jTF_COD_Prod.getText()));
                modelo.addRow(new Object[]{
                    modelo.getRowCount() + 1,
                    modelProdutos.getIdProduto(),
                    modelProdutos.getProNome(),
                    QUANTIDADE,
                    modelProdutos.getProValor(),
                    //Multiplica√ß√£o
                    modelProdutos.getProValor() * QUANTIDADE
                });
                //Passando o valor para campos de TEXTO na tela 
                //poder ser feito o tratamento de String null de 
                //duas formas String.valueOf ou concartenar com aspas dupla (+ "") no final
                //jFTF_ValorTotal.setText(somaValorTotal() + "");
                jFTF_ValorBruto.setText(String.valueOf(somaValorTotal()));
                //limpando o campo
                jTF_COD_Prod.setText("");
                QUANTIDADE = 1;
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(rootPane, "Voc√™ deve informar apena numero!", "ERRR:", JOptionPane.ERROR_MESSAGE);
            }
        }
    }
    /**
     * calculo de somas dos Itens
     * @return soma 
     */
    private float somaValorTotal(){
        float soma = 0, valor = 0;
        int cont = jTbPDV.getRowCount();
        for (int i = 0; i < cont; i++) {
            valor = Float.parseFloat(String.valueOf(jTbPDV.getValueAt(i, 5)));
            soma += valor;
        }
        return soma;
    }
