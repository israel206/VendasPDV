    private void jMI_ExcluirProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMI_ExcluirProdutoActionPerformed
        //ordenando linha depois das exclus√µes
        int quantLinha = jTbPDV.getRowCount();
        //compara√ß√£o ser tem itens na lista  
        if (quantLinha < 1) {
            JOptionPane.showMessageDialog(rootPane, "N√£o existe itens para excluir!", "ATEN√á√ÉO!", JOptionPane.WARNING_MESSAGE);
        } else { // Caso tenha excluar
            DefaultTableModel modelo = (DefaultTableModel)jTbPDV.getModel();
            //Pegando a linha para excluir
            int linha = Integer.parseInt(JOptionPane.showInputDialog("Informe o item que deseja excluir! "));
            // removendo uma linha
            modelo.removeRow(linha-1);
            //somando item restantes
            jFTF_ValorBruto.setText(String.valueOf(somaValorTotal()));

            for (int i = 0; i < quantLinha; i++) {
                modelo.setValueAt(i + 1, i, 0);
            }
        }   
    }//GEN-LAST:event_jMI_ExcluirProdutoActionPerformed

