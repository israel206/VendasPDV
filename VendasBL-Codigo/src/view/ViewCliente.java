    private void btn_salvaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_salvaActionPerformed
        //Atençao Para resolver
        try {
            modelCliente.setIdCliente(Integer.parseInt(this.txt_cliCod.getText()));
        } catch (NumberFormatException e) {
           //JOptionPane.showMessageDialog(rootPane, "Erro ao no código!", "ERRO:", JOptionPane.ERROR_MESSAGE);
           //e.printStackTrace();
        }
        
        modelCliente.setCliNome(this.txt_cliNome.getText());
        modelCliente.setCliZona(this.txt_cliZona.getText());
        modelCliente.setCliEndereco(this.txt_cliEndereco.getText());
        modelCliente.setCliBairro(this.txt_cliBairro.getText());
        modelCliente.setCliCidade(this.txt_cliCidade.getText());
        modelCliente.setCliUf(this.jCBox_cliEstados.getSelectedItem().toString());
        modelCliente.setCliCep(this.jFT_cliCep.getText());
        modelCliente.setCliTelFixo(this.jFTF_cliTel_fixo.getText());
        modelCliente.setCliTelCel(this.jFTF_cliTel_celular.getText());

        if(salvarAlterar.equals("salvar")){
            
            if(controllerCliente.salvarClienteController(modelCliente) > 0){
                JOptionPane.showMessageDialog(rootPane, "Cliente cadastrado com sucesso!", "ATENÇÃO!", JOptionPane.WARNING_MESSAGE);
                this.carregarCliente();
                this.habilitarDesabilitarCampo(false);
                this.limparCampos();
            }else{
                JOptionPane.showMessageDialog(rootPane, "Erro ao cadastrar Cliente!", "ERRO:", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            
            if(controllerCliente.atualizarClienteController(modelCliente)){
                JOptionPane.showMessageDialog(rootPane, "Cliente atualizado com sucesso!", "ATENÇÃO!", JOptionPane.WARNING_MESSAGE);
                
                this.carregarCliente();
                this.habilitarDesabilitarCampo(false);
                this.limparCampos();
                
            }else{
                JOptionPane.showMessageDialog(rootPane, "Erro ao atualizar Cliente!", "ERRO:", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btn_salvaActionPerformed

