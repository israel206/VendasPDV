    private void btn_alteraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_alteraActionPerformed
        salvarAlterar = "alterar";
        int linha = this.tb_cliente.getSelectedRow();
        try {
            int codigoCliete = (int) this.tb_cliente.getValueAt(linha, 0);
            //Recuperar dados do banco
            modelCliente = controllerCliente.getClienteController(codigoCliete);
            //Setar na interface
            txt_cliCod.setText(String.valueOf(modelCliente.getIdCliente()));
            txt_cliNome.setText(modelCliente.getCliNome());
            txt_cliZona.setText(modelCliente.getCliZona());
            txt_cliEndereco.setText(modelCliente.getCliEndereco());
            txt_cliBairro.setText(modelCliente.getCliBairro());
            txt_cliCidade.setText(modelCliente.getCliCidade());
            jCBox_cliEstados.setSelectedItem(modelCliente.getCliUf());
            jFT_cliCep.setText(modelCliente.getCliCep());
            jFTF_cliTel_fixo.setText(modelCliente.getCliTelFixo());
            jFTF_cliTel_celular.setText(modelCliente.getCliTelCel());
            
            habilitarDesabilitarCampo(true);
            this.limparCampos();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Código inválido ou nenhum registro selecionado.", "AVISO!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_alteraActionPerformed

    private void btn_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_excluirActionPerformed
        //selecionar a linha
        int linha = tb_cliente.getSelectedRow();
        //pegando o codigo da coluna da linha selecionada
        int codigoCliente = (int) tb_cliente.getValueAt(linha, 0);
        
        if(controllerCliente.excluirClienteController(codigoCliente)){
           JOptionPane.showMessageDialog(rootPane, "Cliente excluído com sucesso!", "ATENÇÃO!", JOptionPane.WARNING_MESSAGE);
            
           this.carregarCliente();
           this.habilitarDesabilitarCampo(false);
           this.limparCampos();
           
        }else{
            JOptionPane.showMessageDialog(rootPane, "Erro ao excluir Cliente!", "ERRO:", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btn_excluirActionPerformed

