    private void carregarCliente(){
        listaModelCliente = controllerCliente.getListaClienteController();
        //Usando o modelo padrão de tabela
        DefaultTableModel modelo = (DefaultTableModel)tb_cliente.getModel();
        //zerando linha 0 e setando os dados
        modelo.setNumRows(0);
        //inserir produtos na tabela
        int cont = listaModelCliente.size();
        //contador de tamanho da lista
        for (int i = 0; i < cont; i++) {
            //preenchendo col. e linha.
            modelo.addRow(new Object[]{
                listaModelCliente.get(i).getIdCliente(),
                listaModelCliente.get(i).getCliNome(),
                listaModelCliente.get(i).getCliCidade(),
                listaModelCliente.get(i).getCliTelFixo(),
            });
        }
    }
    
